    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         Customer customer = new Customer(Integer.valueOf(TxtSearchID.getText()),TxtName.getText(), TxtContactInfo.getText());
        boolean isaddedItem = CustomerManagementController.addCustomer(customer);
        
        if (isaddedItem) {
            JOptionPane.showMessageDialog(null, "Added!");
        } else {
            JOptionPane.showMessageDialog(null, "Not Added!");
        }
        loadItems();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean updateItem = CustomerManagementController.updateCustomer(
                new Customer(
                       Integer.valueOf(TxtSearchID.getText()),
                        TxtName.getText(),
                        TxtContactInfo.getText()
                      
                        
                ));
 
        if (updateItem) {
            JOptionPane.showMessageDialog(null, "Updated!");
        } else {
            JOptionPane.showMessageDialog(null, "Not Updated!");
        }
        loadItems();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        boolean removecustomer = CustomerManagementController.removeCustomer(TxtSearchID.getText());
        if (removecustomer) {
            JOptionPane.showMessageDialog(null, "Removed!");
        } else {
            JOptionPane.showMessageDialog(null, "Not Removed!");
        }
         clearTxt();
        loadItems();
    }//GEN-LAST:event_jButton3ActionPerformed

