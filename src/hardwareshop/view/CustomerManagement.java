    private void TxtSearchIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtSearchIDKeyReleased
//       int valueOf = Integer.parseInt("0"+TxtSearchID.getText());
       
        Customer searchCustomer = CustomerManagementController.searchCustomer(Integer.parseInt("0"+TxtSearchID.getText()) );
        if (!(null == searchCustomer)) {
            TxtName.setText(searchCustomer.getName());
            TxtContactInfo.setText(searchCustomer.getContactInfo());
           
       

        } else {
            clearTxt();
        }
    }//GEN-LAST:event_TxtSearchIDKeyReleased

