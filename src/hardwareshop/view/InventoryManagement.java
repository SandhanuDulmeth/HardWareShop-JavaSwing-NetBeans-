    private void TxtSearchIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtSearchIDKeyReleased
//       int valueOf = Integer.parseInt("0"+TxtSearchID.getText());
       
        Item searchItem = InventoryManagementController.searchItem(Integer.parseInt("0"+TxtSearchID.getText()) );
        if (!(null == searchItem)) {
            TxtName.setText(searchItem.getName());
            TxtDescription.setText(searchItem.getDescription());
            TxtQuantity.setText(searchItem.getQuantity() + "");
            TxtPrice.setText(searchItem.getPrice() + "");

        } else {
            clearTxt();
        }
    }//GEN-LAST:event_TxtSearchIDKeyReleased

