    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         Item item = new Item(Integer.valueOf(TxtSearchID.getText()),TxtName.getText(), TxtDescription.getText(), Integer.valueOf(TxtQuantity.getText()), Double.valueOf(TxtPrice.getText()));
        boolean isaddedItem = InventoryManagementController.addItem(item);
        
        if (isaddedItem) {
            JOptionPane.showMessageDialog(null, "Added!");
        } else {
            JOptionPane.showMessageDialog(null, "Not Added!");
        }
        loadItems();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean updateItem = InventoryManagementController.updateItem(
                new Item(
                       Integer.valueOf(TxtSearchID.getText()),
                        TxtName.getText(),
                        TxtDescription.getText(),
                        Integer.valueOf(TxtQuantity.getText()),
                        Double.valueOf(TxtPrice.getText())
                        
                ));
 
        if (updateItem) {
            JOptionPane.showMessageDialog(null, "Updated!");
        } else {
            JOptionPane.showMessageDialog(null, "Not Updated!");
        }
        loadItems();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        boolean removeItem = InventoryManagementController.removeItem(TxtSearchID.getText());
        if (removeItem) {
            JOptionPane.showMessageDialog(null, "Removed!");
        } else {
            JOptionPane.showMessageDialog(null, "Not Removed!");
        }
         clearTxt();
        loadItems();
    }//GEN-LAST:event_jButton3ActionPerformed
 public void clearTxt() {
        TxtName.setText(null);
          TxtDescription.setText(null);
        TxtQuantity.setText(null);
        TxtPrice.setText(null);
    }
    

