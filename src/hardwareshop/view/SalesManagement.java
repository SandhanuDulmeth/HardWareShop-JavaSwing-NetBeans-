    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Integer qty = Integer.valueOf("0" + TxtQtySales.getText());
        Item getItem = SalesManagementController.getAllItems().get(jComboBoxSelectItem.getSelectedIndex());
        Customer getCustomer = SalesManagementController.getAllCustomers().get(jComboBoxSelectCustomer.getSelectedIndex());
        Double price = getItem.getPrice();

        TxtTotal.setText((price * qty) + "");
        Sales sales = new Sales(getItem.getItemID(), getCustomer.getCustomerID(), qty, price);
        boolean isaddedItem = SalesManagementController.addItem(sales);

        if (isaddedItem) {
            JOptionPane.showMessageDialog(null, "Added!");
        } else {
            JOptionPane.showMessageDialog(null, "Not Added!");
        }
        loadItems();


    }//GEN-LAST:event_jButton1ActionPerformed

