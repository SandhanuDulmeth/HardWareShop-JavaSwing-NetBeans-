    private void btnFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterActionPerformed
        //ArrayList<Sales> sales = SalesManagementController.getSales();
        Double salesTot = 0d;
        String[] dateComponentsStartInput = StartDate.getText().split("-");
        Integer yearStartInput = Integer.valueOf(dateComponentsStartInput[0]);
        Integer monthStartInput = Integer.valueOf(dateComponentsStartInput[1]);
        Integer dayStartInput = Integer.valueOf(dateComponentsStartInput[2]);

        String[] dateComponentsEndInput = EndDate.getText().split("-");
        Integer yearEndInput = Integer.valueOf(dateComponentsEndInput[0]);
        Integer monthEndInput = Integer.valueOf(dateComponentsEndInput[1]);
        Integer dayEndInput = Integer.valueOf(dateComponentsEndInput[2]);
        for (Sales List : SalesManagementController.getSales()) {
            String dateTime = List.getDate();
            String[] splitDateTime = dateTime.split(" ");
            String date = splitDateTime[0];

            String[] dateComponents = date.split("-");
            Integer year = Integer.valueOf(dateComponents[0]);
            Integer month = Integer.valueOf(dateComponents[1]);
            Integer day = Integer.valueOf(dateComponents[2]);

            if (yearEndInput > year && year > yearStartInput) {
                salesTot += List.getTotalPrice();

            } else if (Objects.equals(yearStartInput, year)) {
                System.out.println("K");
                if ( month > monthStartInput) {
                    System.out.println("Kp");
                    salesTot += List.getTotalPrice();
                } else if (Objects.equals(monthStartInput, month)) {
                    if ( day >= dayStartInput) {

                        salesTot += List.getTotalPrice();
                    }
                }
            }

        }
        lblSalesPrice.setText(Math.round(salesTot) + "");
    }//GEN-LAST:event_btnFilterActionPerformed


